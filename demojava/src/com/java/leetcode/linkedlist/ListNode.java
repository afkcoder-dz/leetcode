package com.java.leetcode.linkedlist;

public class ListNode {
    int val;
    com.java.leetcode.linkedlist.ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }


}
